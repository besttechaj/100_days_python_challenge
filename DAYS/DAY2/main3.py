# python arithmetic operators
'''
# Priority for arithmetic operations: known as PEMDASLR (parenthesis > exponential > multi or division based on LEFT TO RIGHT, WHICHEVER COMES FIRST > ADDITION > SUBTRACTION)
parenthesis: ()
exponents: base ** exponents
multiplication: *
division: /
addition: +
subtraction: -

'''

# addition 
print(2+4)

# subtraction
print(10-8)

# multiplication
print(6*7)

# division: Return type of division is "float"
print(6/3)

# power: base ** exponential
print(3**2)

# applying PEMDASLR RULE FOR BELOW CODE
print(3 * (3 + 3) /  3 - 3); # 3.0